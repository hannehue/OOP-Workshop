import java.util.UUID;

public class VentilatorActuator extends Actuator {

    public VentilatorActuator(String name){
        super(name);
    }

    @Override
    public String getName() {
        return super.getName();
    }

    @Override
    public UUID getId() {
        return super.getId();
    }

    @Override
    public boolean isOn() {
        return super.isOn();
    }
}
